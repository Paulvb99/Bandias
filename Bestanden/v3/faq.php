		<table cellpadding="0" cellspacing="0" width="100%"><tr><td>
			<strong>Vragen &amp; antwoorden:</strong>

	<br /><br />
	<strong><a name="16">01. Als de eigenaar van een station of vliegveld sterft, wat gebeurt er dan?</a></strong><br />
	Momenteel zijn er aan deze gebeurtenis geen consequenties aan verbonden, maar daar wordt aan gewerkt. Op het forum zal daar meer informatie over geven worden door de crew als er veranderingen met betrekking op dit onderwerp plaatsvinden.

	<br /><br />
	<strong><a name="14">02. Als ik me op vakantie status zet, wanneer kan ik weer op mijn account?</a></strong><br />
	Als je op vakantie bent kan je precies 7 dagen niet aangevallen worden. Je kan dan dus ook niet inloggen. Wel krijg je de gebruikelijke drugsupgrades, rente etc.

	<br /><br />
	<strong><a name="35">03. Als je 10 coffeeshops hebt krijg je dan 5.000,- per uur of gewoon 500,- euro?</a></strong><br />

	Je krijgt dan natuurlijk 10 x 500 euro = 5.000 euro per uur.

	<br /><br />
	<strong><a name="20">04. Als je drugsupgrades aanschaft betaal je die 10.000 elke dag opnieuw,of is dat alleen die ene keer wanneer je ze aanschaft?</a></strong><br />
	De drugsupgrades die je doet zijn permanent, dat wil zeggen dat je die elke dag opnieuw krijgt, je betaald 1 keer en krijgt vanaf dan elke dag om 00:10u de hoeveelheid zakjes die in je upgrades vermeld staan.

	<br /><br />
	<strong><a name="38">05. Als je iemand kan aanvallen steel je het dan van de bank of van het contante geld?</a></strong><br />
	Je steelt alleen van het contante geld!

	<br /><br />
	<strong><a name="30">06. Als je iemand vermoord is diegene ook al zijn power en upgrades kwijt?</a></strong><br />

	Momenteel nog niet! Mocht hier verandering in komen dan zal dit via het forum of via de nieuwspagina kenbaar gemaakt worden!

	<br /><br />
	<strong><a name="31">07. Blijven mijn callcredits ook geldig als wij een nieuwe ronde ingaan?</a></strong><br />
	Ja, alles wordt gereset behalve de callcredits en het aantal dagen dat je nog een betaalde account hebt!

	<br /><br />
	<strong><a name="24">08. Helpen de bodyguards voor je bescherming of zijn die alleen maar goed om je power op te krikken?</a></strong><br />
	Ze zijn alleen om extra power te krijgen!

	<br /><br />
	<strong><a name="1">09. Hoe kan ik iemand aanvallen?</a></strong><br />

	Je kan iemand aanval door naar zijn profielpagina te gaan en dan op "Aanvallen" te klikken. Deze persoon moet zich natuurlijk wel in dezelfde stad bevinden! Tevens moet wel minstens 10 killers hebben. Killers krijg je wanneer iemand op je secret link klikt.

	<br /><br />
	<strong><a name="29">10. Hoe kan ik zien hoeveel respectpunten ik heb en hoeveel ik er kan weggeven?</a></strong><br />
	Dit kan je zien op de pagina "Respect" in het menu "Persoonlijk".

	<br /><br />
	<strong><a name="36">11. Hoe kan je meer geld van iemand stelen, ik krijg steeds maar iets van 500 euro?</a></strong><br />
	Hoe hoger het aantal killers dat je hebt en hoe hoger het bedrag is wat degene die je aanvalt contant heeft, des te hoger het bedrag is dat je steelt van degene die je aanvalt.


	<br /><br />
	<strong><a name="5">12. Hoe kom ik aan callcredits?</a></strong><br />

	Je kan callcredits kopen via een SMS of via bellen. Door in het menu "Callcredits" op "Callcredits" te klikken staat er een uitleg hoe je deze kan bestellen.

	<br /><br />
	<strong><a name="28">13. Hoe krijg je respect en hoe geef je het weer weg?</a></strong><br />
	In het profiel van degene die aan wie je een respectpunt wilt geven staat naast de secret link de link 'respect'. Als je klikt op deze link dan geef je een respectpunt weg.

	<br /><br />
	<strong><a name="11">14. Hoe kun je 10 killers krijgen?</a></strong><br />
	Killers kun je winnen door te draaien aan het geluksrad, en door mensen op jouw secret link te laten klikken.

	<br /><br />
	<strong><a name="6">15. Hoe lang duurt het zo ongeveer voor je een SMS'je terug krijgt met de code voor de callcredits?</a></strong><br />

	Normaal gesproken krijg je die meteen terug! Het kan echter soms iets langer duren. Als je na 24u uur nog geen bericht terug ontvangen hebt, kan het beste even contact op nemen met de Admin!

	<br /><br />
	<strong><a name="3">16. Hoe meld je iemand aan via de aanmeld link?</a></strong><br />
	Je stuurt je aanmeld link naar degene die zich via jou link wilt aanmelden.<br /><b>Let op:</b> de aanmeld bonus van 3.000 extra power en 1 extra callcredit wordt automatisch bijgeschreven als de door jou aangemelde persoon promoveert van Groentje naar Loopjongen!

	<br /><br />
	<strong><a name="13">17. Hoe vaak kan je een wapen gebruiken?</a></strong><br />
	Een wapen koop je om je power op te bouwen/  te verhogen. Je kunt hem dus niet gebruiken, hij wordt namelijk automatisch gebruikt. Bij de aankoop ervan wordt namelijk je power eenmalig verhoogd!

	<br /><br />

	<strong><a name="23">18. Hoe weet ik bij welke familie ik terecht kan?</a></strong><br />
	Het beste kan je aan het familiehoofd of een andere Don van de familie waar je bij wil vragen of je daar lid van mag worden en dan zeggen ze ja of nee. De meeste families hebben op het familie profiel de eisen staan, je kan dan vaak een van de Don's een bericht sturen.

	<br /><br />
	<strong><a name="15">19. Ik heb een basis account en heb de 100 procent van de status moordenaar bereikt, en ik krijg maar geen promotie, hoe kan dat?</a></strong><br />
	Om een moordenaar of hoger te worden zul je een betaald account moeten hebben. Zodra je deze hebt zul je verder kunnen stijgen in rang! Een betaalde account heeft nog meer voordelen, zoals; meer rente, je kan vaker een auto proberen te stelen, vaker aan het geluksrad draaien, enzovoort!

	<br /><br />
	<strong><a name="39">20. Ik heb geen drugs of rente ontvangen!</a></strong><br />
	Als je dood bent om 00:10u krijg je geen drugs, rente enzovoort. Overigens, als je drugsupgrades koopt, zul je de nacht erna om 00:10u voor het eerst je zakjes drugs ontvangen!

	<br /><br />

	<strong><a name="2">21. Ik heb nog geen aanmeld bonus gekregen!</a></strong><br />
	De aanmeld bonus (de extra power en callcredit) wordt pas bijgeschreven als de door jouw aangemelde persoon zijn account ook daadwerkelijk activeert! Om fraude te voorkomen wordt tevens de aanmeld bonus niet toegekend als meerdere accounts via &#233;&#233;n computer aangemeld worden!

	<br /><br />
	<strong><a name="4">22. Ik heb vandaag een aantal vrienden aangebracht via mijn aanmeldlink maar ik heb maar 1 callcredit gekregen in plaats van 8. Hoe komt dit?</a></strong><br />
	Je krijgt deze bonus alleen als je iemand aanmeld die zijn account ook daadwerkelijk activeert! Tevens krijg je geen bonus als de door jou aangebrachte leden zich aanmelden op een en dezelfde computer! Dit laatste is (helaas nodig gebleken) om fraude te voorkomen!!

	<br /><br />
	<strong><a name="19">23. Ik heb via de drugs upgrade een aantal zakjes drugs gekocht, wat kan ik hier verder mee doen?</a></strong><br />
	Deze kan je verkopen. De drugs koersen (dit is de verkoopprijs per zakje drugs) in de verschillende steden kun je vinden op drugs koersen pagina.

	<br /><br />

	<strong><a name="33">24. Is het mogelijk dat als ik me bij een familie aansluit en deze me niet bevalt dat ik uit deze familie kan stappen of zitten hier kosten aan?</a></strong><br />
	Dit kan zeker! Een familie kan je verlaten door op de link "Familie verlaten" in het menu "Families" te klikken en is helemaal gratis!

	<br /><br />
	<strong><a name="21">25. Kan ik drugsupgrades ook weer verkopen als ik ze niet meer wil?</a></strong><br />
	Nee, eenmaal een drugsupgrade gekocht kan je deze niet meer verkopen!

	<br /><br />
	<strong><a name="32">26. Kan je een speler ook op naam zoeken?</a></strong><br />
	Ja, dit kan via de pagina "Zoeken" in het menu "Overige"!

	<br /><br />

	<strong><a name="26">27. Kan je geld doneren aan een andere persoon?</a></strong><br />
	Nee, dit is helaas niet mogelijk omdat dit te fraudegevoelig is!

	<br /><br />
	<strong><a name="22">28. Stel je koopt 15 of 30 betaalde dagen en je richt een familie op blijf je nog steeds eigenaar van de familie als je betaald account verloopt?</a></strong><br />
	Ja, je zult na afloop gewoon de eigenaar en oprichter van deze familie blijven!

	<br /><br />
	<strong><a name="17">29. Waar heb je al die drugs voor nodig?</a></strong><br />
	Drugs heb je nodig om winst te maken en dus geld te verdienen om power te kopen. Als je drugs verkoopt kan je het beste goed naar de koersen kijken bij "Drugs koersen" (zodat je het meeste verdient en je drugs niet te goedkoop verkocht wordt)! Na ongeveer een paar dagen heb je je investering terugverdient en kan je winst gaan maken.

	<br /><br />

	<strong><a name="12">30. Waar kan ik bescherming krijgen en hoe?</a></strong><br />
	Dit kan je kopen in de shop of via callcredits in de callcredit shop.

	<br /><br />
	<strong><a name="34">31. Waar kan ik zien wanneer mijn betaald account afloopt?</a></strong><br />
	Dit kan je op de indexpagina zien onder het kopje "Persoonlijke informatie" bij "Betaalde account" (als je tenminste ingelogd bent). Je familie raak je overigens niet kwijt als je betaalde account afloopt!

	<br /><br />
	<strong><a name="18">32. Waar kan je drugs kopen?</a></strong><br />
	Dit kan je doen bij "Drugs Upgrades" in het menu "Criminaliteit". 1 upgrade betekend dat je vanaf de aankoop iedere dag 1 zakje van de gekozen drugs zult ontvangen.

	<br /><br />

	<strong><a name="37">33. Waarom staat er een ster achter mijn naam?</a></strong><br />
	Omdat je een betaalde account hebt!

	<br /><br />
	<strong><a name="40">34. Wat bepaald het succes van een aanval?</a></strong><br />
	Het aantal power dat je verzameld hebt en het aantal killers!

	<br /><br />
	<strong><a name="7">35. Wat heb je aan attack coins?</a></strong><br />
	Attack coins heb je nodig om iemand aan te kunnen vallen. Met 1 attack coin kan je 1 aanval plegen. Als je geen attack coins hebt, kan je dus ook geen mensen aanvallen.

	<br /><br />

	<strong><a name="8">36. Wat is een secret link?</a></strong><br />
	Een secret link is een link waar je killers mee kan verdienen, je hebt 10 killers nodig voordat je mag starten met aanvallen. Per dag kan iedereen 1 keer op je secret link klikken. De secret link kun je doorgeven aan andere mensen en als deze erop klikken vragen ze meestal of je ook op de link van hun wil klikken en zo word je samen sterker. Hoe meer killers je hebt, hoe eerder je aanval zal slagen en hoe sterker je dus wordt!

	<br /><br />
	<strong><a name="27">37. Wat is het voordeel van respect en waar kun je het voor gebruiken?</a></strong><br />
	Respect geven is aantonen dat je iemand respect geeft om zijn/ haar manier van spelen. Dit zijn alleen statistieken en zetten je meer in de schijnwerpers als je in de statistieken voorkomt. Het geeft geen voordeel in enig vorm in het spel.

	<br /><br />
	<strong><a name="25">38. Wat zijn de voordelen van een betaald account?</a></strong><br />
	De voordelen van een betaald account zijn:<br/>

	- Je krijgt een ster achter je naam;<br/>
	- Je kunt de rang van Locale leider, Leider en uiteindelijk Godfather behalen;<br/>
	- Je kan vaker aan het geluksrad draaien;<br/>
	- Je krijgt &euro; 250 per uur extra (100 euro op de bank, en 150 euro contant);<br/>
	- Je krijgt 5% rente per dag (normaal 3%);<br/>
	- Je kan een eigen familie oprichten;<br/>
	- Je mag onbeperkt pagina's bekijken (normaal maximaal 15 per minuut).<br/><br/><br/>
